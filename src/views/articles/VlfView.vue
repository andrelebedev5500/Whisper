            <p>&nbsp;</p>
            <p style="text-align: justify">
              Термин “Великая ложа” в этом тексте, скорее всего, относится к Ассамблее, на которой
              были приняты “Общиe Ордонансы” (или указы), чем к реальному масонскому послушанию. Что
              касается четвёртого градуса, в этом Великая ложа ошибалась, так как историки уже
              доказали, что этот градус пришёл не из Шотландии, а из Англии, так же как и третий
              градус, появившийся незадолго до негo. В Шотландии этот градус не знали, в Англии же,
              он был известен примерно с 1733 года под названием “Шотландский масон”, “Шотландский
              мастер масон” или “Шотландский мастер”.
            </p>
            <p style="text-align: justify">
              В течение последующих лет, в основном во Франции, создаётся множество других, так
              называемых «шотландских» высших градусов. Они встречают большой успех, особенно в
              Бордо, Лионе и Марселе. Великая ложа Франции не способствовала их внедрению, но в
              конечном счёте была вынуждена их признать, как свидетельствует её Регламент от 4 июля
              1755 года, где обладателям звания “Шотландского мастера” предоставляются различные
              привилегии.
            </p>
            <p style="text-align: justify">
              В 1756 году, в конституции лионской ложи “Совершенная Дружба” (фр. La Parfaite Amitie)
              вновь появляется термин “Великая ложа Франции”:
            </p>
            <blockquote class="wp-block-quote is-layout-flow wp-block-quote-is-layout-flow">
              <p>
                <em
                  >«[…] настоящая Конституция утверждена всеми мастерами регулярных лож Парижа,
                  именуемых В. Л. Франции […]»</em
                >
              </p>
            </blockquote>
            <p style="text-align: justify">
              Структура, носящая это название, являлась ещё не совсем истинным масонским
              национальным послушанием, но скорее всего “Великой ложей регулярных мастеров Парижа,
              (или) Франции «или же ложей» Великого мастера всех лож Франции”, решения которой
              распространялись на, примерно, двадцать лож, существующих в Париже, и, более или
              менее, хорошо соблюдались ложами провинций. Великая ложа состояла из Ассамблеи
              мастеров Парижских лож, к которым присоединилось, в зависимости от их переездов,
              некоторое число мастеров из провинциальных лож. В те времена, Досточтимые мастера лож
              не избирались: они получали учредительный патент на свои полномочия и чаще всего
              являлись основоположниками ложи, которой они руководили. В случае, если они являлись
              аристократами или же состоятельными людьми, они предоставляли также помещение и несли
              ответственность за управление. Все это объясняет, почему влияние Великой ложи Франции
              не распространялoсь так бесспорно по всему королевству, подобно современным масонским
              послушаниям. Так создавалась, например, по инициативе Жана-Батиста Виллермоза (фр.
              Jean-Baptiste Willermoz) в 1760 году, “Великая ложа регулярных мастеров Лиона” и, в
              1762 году, “Совершенная ложа Шотландии”, основанная Этьеном Морэном (фр. Etienne
              Morin), в 1745 году в Бордо, и названная <em>Парижской ложей</em> — “Великой
              шотландской ложей Бордо”.
            </p>
            <p style="text-align: justify">
              Начиная c 1760 года, Великая ложа столкнётся с рядом расколов, которые кратковременно
              приостановятся с 1763 по 1766 годы, прежде чем опять возобновятся, а затем приведут, в
              1773 году, через два года после смерти Великого мастера&nbsp;— графа Клермона, к
              разрыву между двумя основными образованиями: Национальное послушание, мажоритарное,
              аристократическое, в высшей степени централизованное, которое примет название Великий
              восток Франции и доверит титул Великого мастера Луи-Филиппу Орлеанскому (фр. Louis
              Philippe d’Orleans, 1747—1793). Подталкиваемое герцогом Люксембургским, заместителем
              Великого мастера, оно (послушание) примет различные реформы, в том числе: выборы
              Досточтимых мастеров в ложах, отстранение «&nbsp;подневольных людей „ и запретит
              собирать ложи в трактирах или в помещениях светского характера. «Великая ложа Клермон»
              (названная в честь Великого мастера скончавшегося в 1771), менее аристократической
              направленности и объединяющая большинство мастеров парижских лож и около полусотни лож
              провинций, оставшихся ей (ВЛ) преданными. Она сохранит старые обычаи, в частности,
              гарантии несменяемости Досточтимых мастеров, но окажется не способна противостоять
              могуществу Великого востока, что позволит ВВФ присоединить к себе до января 1777 года
              первоначально непокорные ложи.
            </p>
            <p style="text-align: justify">
              Французская революция принудит “Великую ложу Клермон” приостановить свои работы в
              ноябре 1792 года. А когда, 17 октября 1796 года, после Террора, работы возобновляются,
              её состав будет включать в себя не более чем из 10 лож в Париже и 8 в провинциях.
              Великий Восток, в свою очередь собравшись 24 февраля 1797 года, не превосходит её в
              численности, несмотря на то, что накануне революции он состоял из болeе чем 600 лож.
              Это ситуация приводёт оба послушания к объединению, после подписания, 22 июня 1799
              года, соглашение об окончательной отмене несменяемости должностей (Досточтимых
              мастеров), при условии о возможности для Досточтимых, ещё от этого не отказавшихся,
              сохранить ещё на девять лет руководство их ложей.
            </p>
            <h2 class="wp-block-heading" style="text-align: justify">
              Великая ложа Франции с 1894 по сегодняшний день
            </h2>
            <p style="text-align: left">
              Верховный совет Франции и “Великая символическая шотландская ложа Франции”, которые
              только что расстались, в 1880 году, соединились вновь, в 1884 году, на заключительном
              банкете Конвента Великого востока Франции и, в 1888 году, их официальные отношения
              были восстановлены.
            </p>
            <p style="text-align: justify">
              В то время, количество Капитулов Верховного совета Франции достигло 23 (4 ?18 градус),
              а число символических лож было 90 (1-3 градус), которые были объединены во внутреннюю
              структуру, называемую “Великой центральной ложей Верховного совета Франции”.
            </p>
            <p style="text-align: justify">
              В 1894 году ложа “Верность” города Лилля собирает ложи севера Франции, и предлагает
              рассмотреть вопрос о возможности объединения голубых лож “Великой центральной ложи”
              для создания нового независимого послушания, которое бы объединилось в свою очередь с
              “Великой символической шотландской ложей Франции”. Собрания других провинциальных лож
              одобрили это предложение и Верховный совет Франции соглашается на проведение
              учредительного Конвента 7 ноября 1894 года. 50-ю голосами против 8-и и 2-х
              воздержавшихся, Конвент голосует за автономию, которая и была предоставлена, что
              привело к созданию нового послушания, взявшее вновь оригинальное название, не
              использовавшееся с 1773 года — Великая ложа Франции. Гюстав Мезюрёр, трёхкратный
